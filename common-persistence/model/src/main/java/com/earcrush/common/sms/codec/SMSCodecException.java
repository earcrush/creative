package com.itsoninc.das.common.sms.codec;

public class SMSCodecException extends Exception {

	private static final long serialVersionUID = 1L;

	public SMSCodecException() {
		super();
	}

	public SMSCodecException(String message, Throwable cause) {
		super(message, cause);
	}

	public SMSCodecException(String message) {
		super(message);
	}

	public SMSCodecException(Throwable cause) {
		super(cause);
	}

}
